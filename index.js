"use strict";
// Declare variables with explicit types
let sname = "John";
let age = 30;
// You can also use the 'const' keyword for immutable values
const pi = 3.14;
// TypeScript can infer types based on the initial value
let isActive = true; // TypeScript infers 'isActive' as a boolean
console.log(sname, age, pi, isActive);
